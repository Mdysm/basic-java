package com.Sept12;

public class While {

	public static void main(String[] args) {
		int i=0;
		while(i<=20) {
			System.out.println(i);
			i+=2;
		}
		System.out.println("====================================================================");
		int j=1;
		while(j<=20) {
			System.out.println(j);
			j+=2;
		}
		
		System.out.println("====================================================================");
		for(j=1;j<=20;j++)
		{
			System.out.println(j);
			j+=1;
		}
	}

}
